public class MainClass {
    public static void main(String[] args) {
        int i = 10;
        int j = 100;

        System.out.println("i + j = " + (i+j));

        /* 기본 자료형 */
        char c = 'a';
        System.out.println("c = " + c);

        int k = 10;
        System.out.println("i = " + k);

        double z = 10.123;
        System.out.println("z = " + z);

        /* 객체 자료형 */
        String str = "Hello Java World!!!!";
        System.out.println(str);
    }
}
